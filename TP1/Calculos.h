
/** \brief              Calcula la suma entre dos numeros.
 *
 * \param n1 float      Variable A.
 * \param n2 float      Variable B.
 * \return float        Devuelve el resultado de la operacion.
 *
 */
float sumar(float n1, float n2);



/** \brief              Calcula la diferencia entre dos numeros.
 *
 * \param n1 float      Variable A.
 * \param n2 float      Variable B.
 * \return float        Devuelve el resultado de la operacion.
 *
 */
float restar(float n1, float n2);



/** \brief              Calcula el producto entre dos numeros.
 *
 * \param n1 float      Variable A.
 * \param n2 float      Variable B.
 * \return float        Devuelve el resultado de la operacion.
 *
 */
float multiplicar(float n1, float n2);



/** \brief              Calcula la division entre dos numeros.
 *
 * \param n1 float      Variable A.
 * \param n2 float      Variable B.
 * \return float        Devuelve el resultado de la operacion.
 *
 */
float dividir(float n1, float n2);



/** \brief                      Calcula el factorial de un numero.
 *
 * \param n int                 Un numero entero mayor o igual a 0.
 * \return unsigned long int    Devuelve el factorial de dicho numero.
 *
 */
unsigned long int sacarFactorial(int n);
